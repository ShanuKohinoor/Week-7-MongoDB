//                      DATABASE
//                      ----*---


//     Database is a system which can store,organizes and manages data.
//     It can easily:-
//          * Save
//          * Update
//          * Delete
//          * Retrieve

//     Uses:-
//          * To store large amounts of data safely.
//          * To find data quickly(search,filter,sort).
//          * To update data without confusion.
//          * To organize information properly.
//          * To allow access data to multiple users at same time.

//    Eg:-
//          * Whatsapp chatHistory
//          * Amazon products List
//          * Phone contact List
//          * Students records in the school
//          * Patients redords in the Hospital


//                                      TYPES OF DATABASE
//                                              |
//                                              |
//                                              |
//                     _________________________|_________________________
//                    |                                                   |
//                    |                                                   |
//                    |                                                   |
//                    V                                                   V
//                   SQL                                                NoSQL
//          (Structed query language)                              (Not only SQL)
//    Also known as Relational Databases(RDBMS)           Also known as Non-Relational Databases




//                                           SQL                   
//                                           ---

//   Features:-
//         * Data stored in tables
//         * Rows(records) and column(fields)
//         * Tables can be linked using relationship
//         * Requires fixed schema(that means structure must be defined)

//  Eg:-
//        * MySQL
//        * PostgreSQL
//        * Oracle
//        * SQL Server

//  This app use case:-
//        * When need accurate financial transaction
//        * Strong consistency
//        * Structured data

//  Uses:-
//        * Banking system
//        * Hospital management
//        * School management 



//                                         NoSQL
//                                         -----

//         * Store Data in flexible documents,key-value pairs,graphs,wide-column models.
//  Features:-
//         * Store data in collecions and documents
//         * No fixed schema(can add fields any time)
//         * Very Flexible
//         * Made for large-scale,fast changing apps

// Eg:-
//         * MongoDB
//         * Firebase
//         * Cassandra
//         * Redis

//  This app use case:-
//         * High speed & flexibility
//         * Frequently changing data
//         * Large scale (millions of users)
//         * Unstructured or semi-structured data
//         * JSON-like documents

// Uses:-
//         * Chat apps
//         * Social media 
//         * Real-time analytics



//                                Difference between SQL and NoSQL
//                                --------------------------------

//                             SQL                                              NoSQL
//                            ----                                             ------       
//   Structure	       * Tables (rows/columns)	                        * Documents (JSON-like)
//   Schema	           * Fixed	                                        * Flexible
//   Relationships	   * Strong (JOINs)                                 * Weak or none
//   Scalability  	   * Vertical (add CPU/RAM)                         * Horizontal (add more servers)
//   Data Type	       * Structured data	                            * Unstructured or semi-structured
//   Best for          * Structured and consistent data                 * Flexible and large scale application
//   Used for          * Banking,                   	                * Real-time apps, 
//                       finance,                                         social media, 
//                       inventory                                        big data



