//                               FIELD UPDATE OPERATORS
//                               ----------------------

//  _________________________________________________________________________________________________________
// |            OPERATOR                   |                          PURPOSE                                |
// |_______________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 1 |         $set                      |  * Update or add a field                                        |        
// |   |                                   |  * Only changes specific field                                  |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 2 |        $unset                     |  * Delete a field                                               |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |                                                                   
// | 3 |         $inc                      |  * Increment or Decrement numeric value                         |
// |   |                                   |  * Create a field if not present                                |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 4 |        $rename                    |  * Change field name                                            |
// |   |                                   |  * Values stays same                                            |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 5 |         $mul                      |  * Multiply numeric field                                       |
// |   |                                   |  * Only affect numbers                                          |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 6 |         $min                      |  * Update if new value is smaller                               |
// |   |                                   |  * Otherwise no change                                          |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 7 |         $max                      |  * Update if new value is greater                               |
// |   |                                   |  * Otherwise no change                                          |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 8 |        $currentDate               |  * Add current Timestamp                                        |
// |   |                                   |  * True:- Date                                                  |
// |   |                                   |  * Type:- Timestamp                                             |
// |___|___________________________________|_________________________________________________________________|
// |                                                                                                         |
// |           Without an operator whole document is replaced except _id.                                    |                                                                
// |_________________________________________________________________________________________________________|                                                                                                       







//                               ARRAY UPDATE OPERATORS
//                               ----------------------
//  _________________________________________________________________________________________________________
// |            OPERATOR                   |                          PURPOSE                                |
// |_______________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 1 |         $push                     |              *  Add value to array end                          |        
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 2 |        $addToSet                  |              * Add unique value only                            |
// |   |                                   |              * If value already present, no change.             |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |                                                                   
// | 3 |         $pull                     |              * Remove matching value/values                     |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 4 |         $pop                      |              * Remove first (-1) or last (1) element            |
// |___|___________________________________|_________________________________________________________________|










//                            Helper operators (with $push/$addToSet)
//                            ---------------------------------------
//  _________________________________________________________________________________________________________
// |            OPERATOR                   |                          PURPOSE                                |
// |_______________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 1 |         $each                     |             * Add multiple values                               |        
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 2 |         $position                 |             * Insert at specific index                          |
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |                                                                   
// | 3 |         $slice                    |             * Limit array size                                  |
// |   |                                   |             * Only works with $push and $each                   |                                                                   
// |___|___________________________________|_________________________________________________________________|
// |   |                                   |                                                                 |
// | 4 |         $sort                     |             * Sort array while updating                         |
// |___|___________________________________|_________________________________________________________________|
// |                                                                                                         |
// |      * Array operators + helper --> control position, order, size                                       |
// |                                                                                                         |
// |     * $position, $slice and $sort are not used with addToSet because addToset only take unique elements.|
// |          - These work with $push because these three hepler operations used on length of the array.     |
// |          - Must use $each in every operation, before using these three helper operation.                |
// |                                                                                                         |
// |_________________________________________________________________________________________________________|                                                                                                       














