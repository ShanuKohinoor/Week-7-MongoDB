// | 2 |          $[elem]                  |         * All elements matching condition                       |
// |   |                                   |         * ArrayFilters required                                 |
// |___|___________________________________|_________________________________________________________________|
// |                                                                                                         |
// |                      Positional $ --> first match only                                                  |
// |                      $[elem] -->  multiple matches                                                      |                                                                
